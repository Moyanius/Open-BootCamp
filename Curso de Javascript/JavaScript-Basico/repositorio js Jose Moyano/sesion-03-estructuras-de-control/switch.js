// Sentencias Switch

let nota = 2;


switch (nota) {
    case 5:
        console.log("Enhorabuena, has obtenido un sobresaliente");
        break;
    case 4:
        console.log("Buen trabajo, pero podiras haberlo hecho mejor");
        break;
    case 3:
        console.log("Has obtenido un suficiente");
        break;
    case 2:
        console.log("No has aprobado por poco");
        break;
    case 1:
        console.log("No has estudiado nada");
        break;
    default:
        console.log("Error")
        break;
} 